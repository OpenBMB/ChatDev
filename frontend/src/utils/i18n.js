function getValue(path, lang) {
  const parts = path.split('.')
  let cursor = messages[lang] || messages.zh
  for (const part of parts) {
    cursor = cursor?.[part]
    if (cursor == null) return undefined
  }
  return cursor
}

export function t(path, vars = {}) {
  const lang = configStore.UI_LANGUAGE === 'en' ? 'en' : 'zh'
  const raw = getValue(path, lang) ?? getValue(path, 'zh') ?? path
  if (typeof raw !== 'string') {
    return raw
  }
  return raw.replace(/\{(\w+)\}/g, (_, key) => String(vars[key] ?? ''))
}

export function useCurrentLanguage() {
  return computed(() => (configStore.UI_LANGUAGE === 'en' ? 'en' : 'zh'))
}
